package beans;

public class PatientBean {
	private String pnum; //患者编号
	private String pname;//姓名
	private String psex;//性别
	private int page;//年龄
	private String pid;//身份证号码
	private String ptel;//手机号码
	private String ppsd;//密码
	private String pinfor;//基本信息
	private String pmdad;//医嘱
	
	public String getPnum() {
		return pnum;
	}
	public void setPnum(String pnum) {
		this.pnum = pnum;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPsex() {
		return psex;
	}
	public void setPsex(String psex) {
		this.psex = psex;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPtel() {
		return ptel;
	}
	public void setPtel(String ptel) {
		this.ptel = ptel;
	}
	public String getPpsd() {
		return ppsd;
	}
	public void setPpsd(String ppsd) {
		this.ppsd = ppsd;
	}
	public String getPinfor() {
		return pinfor;
	}
	public void setPinfor(String pinfor) {
		this.pinfor = pinfor;
	}
	public String getPmdad() {
		return pmdad;
	}
	public void setPmdad(String pmdad) {
		this.pmdad = pmdad;
	}
	
	

	
}
